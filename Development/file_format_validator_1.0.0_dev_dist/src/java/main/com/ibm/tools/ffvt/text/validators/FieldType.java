package com.ibm.tools.ffvt.text.validators;

import com.ibm.tools.ffvt.text.validators.*;

/**
 * This is an enumeration for field types supported by FieldTypeValidator.
 * 
 * Thread Safety:
 * This class is immutable and thread safe.
 */
public class FieldType {
    /**
     * Represents the integer field type.
     */
    public FieldType INTEGER;

    /**
     * Represents the big decimal field type.
     */
    public FieldType BIG_DECIMAL;

    /**
     * Represents the date field type.
     */
    public FieldType DATE;
}

